package Programms;

public class FindingDateUsingSwitch {

	public static void main(String[] args) {
		
		int BirthDate = 13;
	
		switch(BirthDate){    
		case 14 :    
			System.out.println("Harish's birthday");;
		     break;
		case 13:    
		   System.out.println("Nitesh's birthday");
		 break; 
	  
		   
		default:     
		  System.out.println("No one has birthday on 13");
		}    

     
	}

}
